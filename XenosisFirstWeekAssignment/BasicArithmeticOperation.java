package XenosisFirstWeekAssignment;

public class BasicArithmeticOperation {
    public static void main(String[] args) {
        System.out.println("Perform some Basic Arithmetic Operation");
        int n1 = 12;
        int n2 = 22;
        System.out.println("Perform Addition");
        int sum = n1+n2;
        System.out.println("addition of two number "+ sum);

        System.out.println("--------------------------------------------------------------");

        System.out.println("Perform Subtraction");
        int sub = n1-n2;
        System.out.println("addition of two number "+ sub);

        System.out.println("--------------------------------------------------------------");

        System.out.println("Perform multiply");
        long multi = n1*n2;
        System.out.println("addition of two number "+ multi);

        System.out.println("--------------------------------------------------------------");

        System.out.println("Perform Division ");
        double divide = n1/n2;
        System.out.println("addition of two number "+ divide);
    }
}
